package cn.waimai.pojo;

public class UserInfo {
	private String uid;
	private String user_name;
	private String user_mobile;
	private String user_set;
	private Integer user_shopCar;
	private Integer user_form;
	private Integer user_history;
	private Integer user_member;
	private Integer user_comment;
	private Integer user_refund;

	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_mobile() {
		return user_mobile;
	}
	public void setUser_mobile(String user_mobile) {
		this.user_mobile = user_mobile;
	}
	public String getUser_set() {
		return user_set;
	}
	public void setUser_set(String user_set) {
		this.user_set = user_set;
	}
	public Integer getUser_shopCar() {
		return user_shopCar;
	}
	public void setUser_shopCar(Integer user_shopCar) {
		this.user_shopCar = user_shopCar;
	}
	public Integer getUser_form() {
		return user_form;
	}
	public void setUser_form(Integer user_form) {
		this.user_form = user_form;
	}
	public Integer getUser_history() {
		return user_history;
	}
	public void setUser_history(Integer user_history) {
		this.user_history = user_history;
	}
	public Integer getUser_member() {
		return user_member;
	}
	public void setUser_member(Integer user_member) {
		this.user_member = user_member;
	}
	public Integer getUser_comment() {
		return user_comment;
	}
	public void setUser_comment(Integer user_comment) {
		this.user_comment = user_comment;
	}
	public Integer getUser_refund() {
		return user_refund;
	}
	public void setUser_refund(Integer user_refund) {
		this.user_refund = user_refund;
	}
}
	